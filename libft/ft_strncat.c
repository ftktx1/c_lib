char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int	s2_i;
	int	s1_i;

	s2_i = 0;
	s1_i = 0;
	while (s1[s1_i])
		s1_i++;
	while (s2[s2_i] != '\0' && s2_i < n)
	{
		s1[s1_i] = s2[s2_i];
		s2_i++;
		s1_i++;
	}
	s1[s1_i] = '\0';
	return (s1);
}
