void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*tmp;

	tmp = (char *)dst;
	while (n > 0)
	{
		*tmp = *((char *)src);
		tmp++;
		src++;
		n--;
	}
	return (dst);
}
