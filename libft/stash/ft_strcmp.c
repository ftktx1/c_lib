int	ft_strcmp(const char *s1, const char *s2)
{
	int	cntr;

	cntr = 0;
	while (s1[cntr] == s2[cntr] && s1[cntr] != '\0' && s2[cntr] != '\0')
		cntr++;
	if (s1[cntr] == '\0' && s2[cntr] == '\0')
		return (0);
	else
		return (s1[cntr] - s2[cntr]);
}
